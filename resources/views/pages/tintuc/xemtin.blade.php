                    <p style="font-weight: 400;">Sáng ngày 16/01/2024, tại Hà Nội đã diễn ra phiên họp Hội đồng quản lý Quỹ Bảo vệ và Phát triển rừng Việt Nam. Ông Trần Quang Bảo, Cục trưởng Cục Lâm nghiệp, Chủ tịch Hội đồng chủ trì cuộc họp.</p>
                    <p style="font-weight: 400;">Phiên họp có sự tham dự của các thành viên Hội đồng quản lý, Ban Kiểm soát, Ban Điều hành Quỹ và đại diện các lãnh đạo, chuyên viên của Cục, Vụ liên quan.</p>
                    <p style="font-weight: 400;">Tại buổi họp, Ban Điều hành Quỹ (Quỹ Trung ương) đã báo cáo tình hình triển khai thực hiện nhiệm vụ năm 2023 và đề xuất một số nhiệm vụ trọng tâm năm 2024. Theo báo cáo của Quỹ Trung ương, được sự quan tâm, chỉ đạo sát sao của các cấp lãnh đạo, sự phối hợp, hợp tác chặt chẽ với các đơn vị, đối tác liên quan, năm 2023, toàn hệ thống Quỹ Bảo vệ và phát triển rừng (BV&amp;PTR) ghi nhận nhiều kết quả tích cực, đáng khích lệ.</p>
                    <p style="font-weight: 400;">Trong năm 2023, Quỹ Trung ương tích cực đề xuất, tham mưu, tiếp thu, giải trình nội dung về dịch vụ môi trường rừng (DVMTR) và Điều 72 a (thí điểm về dịch vụ hấp thụ và lưu giữ các-bon của rừng) trong dự thảo Nghị định sửa đổi, bổ sung một số điều của Nghị định số 156/2018/NĐ-CP ngày 16/11/2018; tham gia xây dựng Thông tư số 22/2023/TT-BNNPTNT ngày 15/12/2023 nội dung về trồng rừng thay thế và Thông tư số 23/2023/TT-BNNPTNT, ngày 15/12/2023 quy định đo đạc, báo cáo, thẩm định kết quả giảm nhẹ phát thải khí nhà kính và kiểm kê khí nhà kính lĩnh vực lâm nghiệp.</p>
                    <p style="font-weight: 400;">Năm qua, ảnh hưởng của biến đổi khí hậu đã làm giảm công suất và sản lượng điện của các nhà máy thủy điện, đặc biệt là một số nhà máy thủy điện lớn như Lai Châu, Sơn La, Hòa Bình&#8230; dẫn đến ảnh hưởng đến nguồn thu tiền DVMTR. Với 1.585 hợp đồng ủy thác chi trả tiền DVMTR, cả nước đã thu được 3.158,52 tỷ đồng tiền DVMTR theo Nghị định số 156 (đạt 98,7% kế hoạch năm 2023).</p>
                    <p style="font-weight: 400;">Đặc biệt, năm 2023, ghi nhận sự kiện quan trọng khi lần đầu tiên, Việt Nam chuyển nhượng thành công 10,3 triệu tấn các-bon giảm phát thải từ rừng vùng Bắc Trung Bộ theo Nghị định số 107/2022/NĐ-CP (ERPA) và hiện tại, đã thu về 997,03 tỷ đồng (tương đương 80% lượng giảm phát thải đã ký kết) và được bình chọn là một trong 10 sự kiện nổi bật của ngành nông nghiệp 2023. </p>
                    <p style="font-weight: 400;">Năm 2023, diện tích rừng được hưởng tiền DVMTR là 7,28 triệu ha, chiếm 49,24% tổng diện tích rừng toàn quốc. Hơn 3.334 tỷ đồng tiền DVMTR đã được chi trả cho 238 Ban quản lý rừng đặc dụng, phòng hộ; 102 công ty lâm nghiệp; 1.392 UBND cấp xã; 375 chủ rừng khác là đơn vị công an, bộ đội, doanh nghiệp và trung tâm nghiên cứu; 240.345 chủ rừng là cá nhân, hộ gia đình, cộng đồng, thôn bản chủ yếu qua hình thức tài khoản ngân hàng, giao dịch điện tử và bưu chính, đảm bảo tính công khai, minh bạch, hiệu quả. Tiền DVMTR đã góp phần hỗ trợ cho chủ rừng có kinh phí cho quản lý, bảo vệ rừng; giúp các công ty lâm nghiệp, Ban quản lý rừng có nguồn tài chính duy trì hoạt động trong bối cảnh dừng khai thác gỗ rừng tự nhiên và góp phần nâng cao đời sống, sinh kế cho người dân miền núi sống phụ thuộc vào rừng.</p>
                    <p style="font-weight: 400;">Đối với nguồn thu từ ERPA, Quỹ Trung ương đã tạm ứng hơn 962 tỷ đồng cho 6 Quỹ BV&amp;PTR tỉnh vùng Bắc Trung Bộ để các tỉnh nhanh chóng xây dựng kế hoạch triển khai chi trả cho các chủ rừng và các đối tượng hưởng lợi theo đúng quy định. Trong thời gian tới, Bộ NN&amp;PTNT tiếp tục phối hợp chặt chẽ với Ngân hàng Thế giới hoàn thiện các thủ tục cần thiết để tiếp nhận số tiền còn lại theo ERPA (20% lượng giảm phát thải còn lại đã ký kết). Nguồn thu từ ERPA sẽ góp phần bổ sung nguồn tài chính hỗ trợ công tác quản lý, bảo vệ, phát triển và nâng cao hệ sinh thái rừng bền vững, bảo tồn đa dạng sinh học của rừng, hỗ trợ phát triển sinh kế cho đồng bào nghèo làm nghề rừng, sống gắn bó với rừng trên địa bàn thí điểm.</p>
                    <p style="font-weight: 400;">Về một số công tác khác như kiểm tra, giám sát, truyền thông và hợp tác quốc tế vẫn luôn được xác định là nhiệm vụ trọng tâm, xuyên suốt trong quá trình thực hiện chính sách, góp phần nâng cao hiệu quả việc triển khai chính sách trên cả nước. Tình hình thực hiện nghĩa vụ nộp tiền DVMTR của các đơn vị sử dụng DVMTR đã có nhiều chuyển biến tích cực khi phần lớn các đơn vị đều chấp hành đúng quy định của pháp luật, thể hiện ý thức, trách nhiệm của các bên liên quan đã được nâng cao hơn.</p>
                    <p style="font-weight: 400;">Bên cạnh những kết quả đạt được, cũng còn một số khó khăn, hạn chế trong quá trình triển khai chính sách như một số Quỹ BV&amp;PTR tỉnh có bộ máy tổ chức chưa hoàn thiện, kiêm nhiệm; chi trả tiền DVMTR qua tài khoản ngân hàng cho các hộ gia đình, đối với khu vực miền núi còn gặp nhiều khó khăn do chỉ có thể mở tài khoản cá nhân&#8230;</p>
                    <p style="font-weight: 400;">Phát biểu tại phiên họp, các thành viên hội đồng quản lý và ban kiểm soát đánh giá cao kết quả đạt được của Ban Điều hành trong năm qua, đặc biệt là việc triển khai thí điểm chuyển nhượng kết quả giảm phát thải và quản lý tài chính thỏa thuận chi trả giảm phát thải khí nhà kính vùng Bắc Trung Bộ, lần đầu tiên được thể chế hóa tại Nghị định số 107 và thực hiện ở Việt Nam, đánh dấu mốc về bước khởi đầu mở ra tiềm năng dịch vụ hấp thụ và lưu giữ các-bon của rừng của ngành lâm nghiệp nói riêng và của Việt Nam nói chung. Ông Trần Quang Bảo, Cục trưởng Cục Lâm nghiệp, Chủ tịch Hội đồng quản lý nhấn mạnh một số nhiệm vụ cần chú trọng trong năm 2024 như: tập trung thu đúng, thu đủ tiền DVMTR đảm bảo mục tiêu đạt 3.200 tỷ đồng và giải ngân tiền DVMTR đầy đủ, đúng đối tượng, đúng mục đích, kịp thời đảm bảo duy trì 7,3 triệu ha rừng trong lưu vực cung ứng DVMTR; tiếp tục tiếp thu, giải trình nội dung DVMTR và Điều 72a trong dự thảo Nghị định sửa đổi, bổ sung Nghị định số 156 nhằm khai thác tiềm năng, góp phần đa dạng hóa nguồn thu DVMTR; nghiên cứu, đánh giá tác động tăng mức chi trả tiền DVMTR đối với cơ sở sản xuất thủy điện và nước sạch; thúc đẩy, hướng dẫn triển khai Nghị định số 107 có hiệu quả&#8230;</p>
